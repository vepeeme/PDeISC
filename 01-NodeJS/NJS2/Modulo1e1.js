//exporta una funcion que suma a y b
export function sumar(a,b) {
    return a + b;
}
//exporta una funcion que resta a y b
export function restar(a,b) {
    return a - b;
}
//exporta una funcion que multiplica a y b
export function multiplicar(a,b) {
    return a * b;
}
//exporta una funcion que division a y b
export function dividir(a,b) {
    return a / b;
}
